'''
재귀적인 패턴으로 별을 찍어 보자. 
N이 3의 거듭제곱(3, 9, 27, ...)이라고 할 때,
 크기 N의 패턴은 N×N 정사각형 모양이다.
1 ≤ N ≤ 100
첫째 줄부터 2×N-1번째 줄까지 차례대로 별을 출력한다.
***************************
* ** ** ** ** ** ** ** ** *
***************************
***   ******   ******   ***
* *   * ** *   * ** *   * *
***   ******   ******   ***
***************************
* ** ** ** ** ** ** ** ** *
***************************
*********         *********
* ** ** *         * ** ** *
*********         *********
***   ***         ***   ***
* *   * *         * *   * *
***   ***         ***   ***
*********         *********
* ** ** *         * ** ** *
*********         *********
***************************
* ** ** ** ** ** ** ** ** *
***************************
***   ******   ******   ***
* *   * ** *   * ** *   * *
***   ******   ******   ***
***************************
* ** ** ** ** ** ** ** ** *
***************************
'''
string = ''

def star(n):
    global string
    if n < 3:
        return '*'

    for _ in range(n):
        string += f'{star(n//3) * 3}\n{star(n//3)} {star(n//3)}\n{star(n//3)*3}'
    return string

print(star(27))
    