package ch04;

public class binaryTest {

	public static void main(String[] args) {
		// 2진수, 8진수 16진수를 프로그램에서 사용
		int num = 10;
		int bNum = 0B1010;
		int oNum = 012;
		int xNum = 0XA;
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);
		
		//		10
		//		10
		//		10
		//		10		
	}

}
